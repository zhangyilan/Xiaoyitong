package maplabeing.entity;

public class Parks {
private int id;
private String parkname;
private Double parklatitude;
private Double parklongitude;
private String parktotalnumber;
private String parkusenumber;
private String parkimage;
private String parkaddress;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getParkname() {
	return parkname;
}
public void setParkname(String parkname) {
	this.parkname = parkname;
}
public Double getParklatitude() {
	return parklatitude;
}
public void setParklatitude(Double parklatitude) {
	this.parklatitude = parklatitude;
}
public Double getParklongitude() {
	return parklongitude;
}
public void setParklongitude(Double parklongitude) {
	this.parklongitude = parklongitude;
}
public String getParktotalnumber() {
	return parktotalnumber;
}
public void setParktotalnumber(String parktotalnumber) {
	this.parktotalnumber = parktotalnumber;
}
public String getParkusenumber() {
	return parkusenumber;
}
public void setParkusenumber(String parkusenumber) {
	this.parkusenumber = parkusenumber;
}
public String getParkimage() {
	return parkimage;
}
public void setParkimage(String parkimage) {
	this.parkimage = parkimage;
}
public String getParkaddress() {
	return parkaddress;
}
public void setParkaddress(String parkaddress) {
	this.parkaddress = parkaddress;
}
}
